
package flagapolska;
    

public class Student {
    private int nr_indeksu;
    private float ocena;
    
    public Student(int nr_indeksu, float ocena){
        this.nr_indeksu = nr_indeksu;
        this.ocena = ocena;
    }

    public int getNr_indeksu() {
        return nr_indeksu;
    }

    public float getOcena() {
        return ocena;
    }
       
    
}
